package util;

/**
 * This class contains method that executes maths operation.
 * We can add new methods for another operations like multiplication or division
 * (we can add in methods some validation or other logic)
 */
class MathFunctions {

    static int plus(int[] numbers) {
        return numbers[0] + numbers[1];
    }

    static int minus(int[] numbers) {
        return numbers[0] - numbers[1];
    }
}
